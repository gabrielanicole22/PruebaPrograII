    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        try {
            int codigo = Integer.parseInt(txtCodigo.getText());
            String nombre = txtNombre.getText();
            String tipo = txtTipo.getText();
            if (nombre.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Por favor, rellene todos los campos.");
                return;
            }

            if (blockB.buscarItem(codigo, nombre) != null) {
                JOptionPane.showMessageDialog(null, "¡¡¡Ya existe un item con el código " + codigo + ".\nNo se pudo agregar!!!");
            } else {
                blockB.agregarItem(codigo, nombre, tipo);
                JOptionPane.showMessageDialog(null, "¡¡¡Item agregado con éxito.!!!");
                this.dispose();
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Ingrese un valor numérico válido para el código.");
        }    
    }//GEN-LAST:event_btnGuardarActionPerformed


